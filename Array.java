package arrayDemo;

public class Array {

	public static void main(String[] args) {
		
String[] animals = {"Lion", "Tiger", "Elephant", "Giraffe", "Zebra", "Monkey", "Kangaroo", "Panda", "Penguin", "Dolphin"};
        System.out.println("List of animals:");
        for (String animal : animals) {
            System.out.println(animal);
        }

	}

}
